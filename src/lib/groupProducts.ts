/**
 * Extract the painting slug and material from a Shopify product handle.
 * Returns null if no known material suffix is found (→ non-art product).
 *
 * Tolerates Shopify's auto-disambiguation suffix (`-1`, `-2`, …) that gets
 * appended when a duplicate handle is created — these don't mean the product
 * is different, they mean Shopify had to rename it. So
 * "soul-gathering-framed-poster-2" parses identically to
 * "soul-gathering-framed-poster".
 *
 * Example: "double-portrait-pink-framed-poster" → { slug: "double-portrait-pink", material: "Framed Poster" }
 */
function parsePaintingFromHandle(handle: string): { slug: string; material: Material } | null {
  // Strip a trailing "-<digits>" disambiguation suffix before matching.
  const stripped = handle.replace(/-\d+$/, "");
  for (const { suffix, material } of HANDLE_MATERIAL_SUFFIXES) {
    if (stripped.endsWith(`-${suffix}`)) {
      return { slug: stripped.slice(0, -(suffix.length + 1)), material };
    }
  }
  return null;
}

/** Convert a kebab-case slug to Title Case for display.
 *  Example: "double-portrait-on-pink" → "Double Portrait On Pink" */
function titleCaseFromSlug(slug: string): string {
  return slug
    .split("-")
    .map((w) => w.charAt(0).toUpperCase() + w.slice(1))
    .join(" ");
}

/**
 * Resolve the canonical painting name + material for a Shopify product.
 *
 * Strategy (priority order):
 *
 * 1. HANDLE + PRODUCTTYPE — locale-independent primary path.
 *    Shopify handles are immutable URL slugs set when the product was created;
 *    they never change with locale. We strip the material suffix from the handle
 *    to get a stable painting slug that is the same for all variants of the same
 *    painting in all languages.
 *    productType is also normalised across locales using MATERIAL_ALIASES_I18N.
 *
 *    This eliminates the core problem: inconsistent Shopify translations across
 *    product variants (e.g. "Retrato doble sobre rosa" vs "Doble retrato sobre
 *    rosa" for the same painting) no longer matter — we never use the title for
 *    the grouping key.
 *
 *    Display name: image-map canonical → English title parse → title-cased slug.
 *
 * 2. IMAGE-MAP + TITLE PARSE — fallback for products without a recognisable
 *    handle suffix (non-art items, custom handles, etc.).
 */
function resolvePainting(product: ShopifyProduct): { painting: string; material: Material } | null {
  const fromImage = PAINTING_MAP[product.handle];
  // parseTitle is locale-aware AND falls back to suffix-style parsing for
  // titles like "Narwa Premium Matte Paper" (no separator).
  const parsed = parseTitle(product.title);

  // ── Primary path: handle suffix carries the material ─────────────────────
  const fromHandle = parsePaintingFromHandle(product.handle);
  const handleMaterial = fromHandle?.material ?? normaliseMaterial(product.productType);

  if (fromHandle && handleMaterial) {
    const paintingName =
      fromImage?.painting
        ? canonicalize(fromImage.painting)
        : parsed?.painting
          ? canonicalize(parsed.painting)
          : canonicalize(titleCaseFromSlug(fromHandle.slug));
    return { painting: paintingName, material: handleMaterial };
  }

  // ── Image-map fallback ───────────────────────────────────────────────────
  if (fromImage?.painting) {
    const fallbackMaterial =
      parsed?.material ??
      normaliseMaterial(product.productType) ??
      "Poster";
    return { painting: canonicalize(fromImage.painting), material: fallbackMaterial };
  }

  // ── Title-only fallback (parseTitle handled it) ──────────────────────────
  if (parsed) {
    return { painting: canonicalize(parsed.painting), material: parsed.material };
  }

  // ── Last-resort fallback: handle has no recognised suffix AND title can't
  //    be parsed, but productType still says it's a print material. Treat the
  //    whole handle as the painting slug. Used by clean handles like "narwa"
  //    (no material in the URL) where productType = "Print Material" or
  //    similar tells us what we're looking at. */
  const productMaterial = normaliseMaterial(product.productType);
  if (productMaterial) {
    return {
      painting: canonicalize(titleCaseFromSlug(product.handle)),
      material: productMaterial,
    };
  }

  return null;
}

/** Returns the virtual master handle for a given source product, or null
 *  if the product isn't an art/painting product. Used by the detail page
 *  to redirect legacy URLs to the new master URL.
 *
 *  When `englishCanonicalMap` is supplied (any non-EN locale render),
 *  the handle uses the EN canonical painting name so master URLs are
 *  identical across locales — i.e. /pt/shop/the-ceremony, never
 *  /pt/shop/a-cerimonia. The bucket key in groupProductsByPainting uses
 *  the same source, so the redirect always lands on a real master. */
export function getMasterHandle(
  product: ShopifyProduct,
  englishCanonicalMap?: EnglishPaintingMap,
): string | null {
  const englishPainting = englishCanonicalMap
    ? lookupEnglishPainting(product, englishCanonicalMap)
    : undefined;
  if (englishPainting) return slugify(englishPainting);
  const resolved = resolvePainting(product);
  return resolved ? slugify(resolved.painting) : null;
}

