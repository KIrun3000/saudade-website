/**
 * Virtual product grouping for the Saudade Voces art shop.
 *
 * Shopify currently stores each (painting × material) as a separate product
 * (e.g. "Soul Gathering — Canvas", "Soul Gathering — Framed Poster"). We want
 * the storefront to show ONE master product per painting, with variants on
 * Material × Frame × Size. This module synthesises those master products
 * client-side from the raw Shopify list — no Shopify import needed.
 *
 * The synthesised "virtual" products preserve the real Shopify variant IDs,
 * so AddToCart / checkout continues to work unchanged.
 *
 * Decisions baked in (per Maya, 2026-04-25):
 * - Master title = canonical painting name (no " — Material" suffix).
 * - Variant axes: Material × Frame × Size.
 * - "Assembly: Not assembled" Framed Poster variants are dropped; framed
 *   items default to Ready-to-hang.
 * - "Nawra" is "Narwa" misspelt; merged.
 * - Orientation (Vertical/Horizontal) is folded into Size as a prefix.
 * - Non-art products (clothing, etc.) pass through untouched.
 */

