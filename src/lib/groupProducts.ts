/** Parse "Painting Name — Material" → { painting, material }. Locale-aware:
 *  the material token can be any known translation (English / Spanish /
 *  Portuguese / Polish), so titles like "A Cerimônia — Tela Emoldurada" or
 *  "La Ceremonia — Póster Enmarcado" parse correctly.
 *
 *  Falls back to a separator-less suffix match for titles like "Narwa Premium
 *  Matte Paper" (no dash) — common when products come from Gelato/Printful
 *  integrations that name SKUs after the print medium without punctuation. */
function parseTitle(title: string): { painting: string; material: Material } | null {
  const trimmed = title.trim();

  // Step 1 — explicit dash separator (em / en / hyphen).
  const m = trimmed.match(/^(.*?)\s+[—–-]\s+(.+?)\s*$/);
  if (m) {
    const rawMaterial = m[2].trim();
    const localised = normaliseMaterial(rawMaterial);
    if (localised) return { painting: m[1].trim(), material: localised };
    if ((MATERIALS as readonly string[]).includes(rawMaterial)) {
      return { painting: m[1].trim(), material: rawMaterial as Material };
    }
  }

  // Step 2 — no separator, but the end of the title matches a known material
  // alias. Tries longest aliases first so "Framed Premium Matte Paper" wins
  // before "Premium Matte Paper" and "Matte Paper".
  const lower = trimmed.toLowerCase();
  const aliases = Object.keys(MATERIAL_ALIASES_I18N).sort(
    (a, b) => b.length - a.length,
  );
  for (const alias of aliases) {
    if (lower.endsWith(alias)) {
      const material = MATERIAL_ALIASES_I18N[alias];
      const cut = trimmed.length - alias.length;
      const painting = trimmed.slice(0, cut).replace(/[\s—–\-:|·]+$/, "").trim();
      if (painting) return { painting, material };
    }
  }

  return null;
}

function canonicalize(painting: string): string {
  return TITLE_ALIASES[painting] ?? painting;
}

function slugify(s: string): string {
  return s
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/^-+|-+$/g, "");
}

function findOption(opts: ShopifySelectedOption[], name: string): string | undefined {
  // Case-insensitive name match across every known locale alias for `name`.
  // Shopify translates option names ("Frame"→"Marco"/"Moldura"), so the
  // English string by itself is not enough on ES/PT/PL detail pages.
  const aliases = OPTION_NAME_ALIASES[name] ?? [name.toLowerCase()];
  return opts.find((o) => aliases.includes(o.name.toLowerCase()))?.value;
}

// Patterns that strongly suggest a given option is Frame / Size, used as
// VALUE-based fallbacks when the option's name is non-standard. Conservative
// on purpose — false positives would cause Material/Orientation/Assembly to
// be misclassified.
const FRAME_NAME_RE = /frame|marco|moldura|rama|ramka/i;
const FRAME_VALUE_RE =
  /\b(frame|marco|moldura|rama|black|white|wood|dark wood|negro|negra|blanco|madera|preto|preta|branco|branca|madeira|czarn|biał|biala|drewn)/i;
const SIZE_NAME_RE = /size|tamañ|tamano|tamanh|rozmiar|measur|medid|wymiar|format/i;
const SIZE_VALUE_RE = /\d+\s*[x×]\s*\d+|\d+\s*(?:cm|mm|in|inch|"|″)\b/i;
const ORIENT_NAME_RE = /orient/i;
const ORIENT_VALUE_RE = /\b(vertical|horizontal|pionow|poziom|vertik|horizont)/i;

/** Find the variant's frame value robustly:
 *  1) by the option named "Frame" (any locale alias);
 *  2) failing that, by any option NAME that hits the frame keyword regex;
 *  3) failing that, by any option VALUE that looks like a frame colour
 *     ("Wood frame" / "Marco de madera" / "Czarny" / "Madeira"). */
function findFrameValue(opts: ShopifySelectedOption[]): string | undefined {
  const direct = findOption(opts, OPTION_FRAME);
  if (direct) return direct;
  const byNameContains = opts.find((o) => FRAME_NAME_RE.test(o.name))?.value;
  if (byNameContains) return byNameContains;
  return opts.find((o) => FRAME_VALUE_RE.test(o.value))?.value;
}

/** Find the variant's size value robustly:
 *  1) by the option named "Size" (any locale alias);
 *  2) failing that, by any option NAME that hits the size keyword regex;
 *  3) failing that, by any option VALUE that looks like a size
 *     ("30x40 cm", "8×10\"", "30 x 40"). */
function findSizeValue(opts: ShopifySelectedOption[]): string | undefined {
  const direct = findOption(opts, OPTION_SIZE);
  if (direct) return direct;
  const byNameContains = opts.find((o) => SIZE_NAME_RE.test(o.name))?.value;
  if (byNameContains) return byNameContains;
  return opts.find((o) => SIZE_VALUE_RE.test(o.value))?.value;
}

/** Find the variant's orientation value robustly. Same pattern as the others. */
function findOrientationValue(opts: ShopifySelectedOption[]): string | undefined {
  const direct = findOption(opts, OPTION_ORIENTATION);
  if (direct) return direct;
  const byNameContains = opts.find((o) => ORIENT_NAME_RE.test(o.name))?.value;
  if (byNameContains) return byNameContains;
  return opts.find((o) => ORIENT_VALUE_RE.test(o.value))?.value;
}

/** Normalise a frame value; empty/missing → "None". */
function normaliseFrame(value: string | undefined): string {
  return value && value.trim() ? value.trim() : "None";
}

/** Combine size + orientation into a single Size axis value. */
function normaliseSize(size: string | undefined, orientation: string | undefined): string {
  const s = (size ?? "").trim();
  const o = (orientation ?? "").trim();
  if (s && o) return `${o} · ${s}`;
  return s || o || "Default";
}

